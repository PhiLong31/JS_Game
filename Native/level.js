import Bricks from "./bricks.js";

export function buildLevel(game, level){
    let bricks = [];

    level.forEach((row, rowIndex) =>{
        row.forEach((brick, brickIndex) =>{
            if(brick === 1)[
                bricks.push(new Bricks(game, {x: 40 * brickIndex, y : 30 + 24 * rowIndex}))
            ]
        })
    })

    return bricks;
}

